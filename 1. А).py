import numpy as np
from random import randint
d = [randint(-10,10) for i in range(15)] #генеруємо список з 15 елементів від -10 до 10
a = np.array(d) #перетворюємо список в масив
b = a[::-1] #робимо зріз, який буде записаний у зворотньому порядку
print(a) #виводимо оригынальний масив і зріз, щоб була видна різниця
print(b)
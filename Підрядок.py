'''
Пошук підрядка в рядку
'''
a = input('Введіть рядок: ') #вводимо рядок
b = input('Введіть шуканий підрядок: ') #вводимо шуканий підрядок
i = a.find(b) #перевірямо наявність підрядка функцією find()
if i >= 0:
    print('Збіг з', i,'елемента')
    '''
    так як функція find() повертає індекс першого входження, то у випадку невходження 
    повертається індекс -1
    '''
else:
    print('Збігів немає')